def map_get(board_map: list[list[list[int | None]]], x: int, y: int, z: int) -> str | None:
    if x < 0 or y < 0 or z < 0:
        return None
    try:
        tile = board_map[z][y][x]
    except IndexError:
        return None
    return None if tile is None else str(tile)


def map_width() -> int:
    return len(RESPONSIVE_MAP[0][0])


def map_height() -> int:
    return len(RESPONSIVE_MAP[0])


def get_map(limit_tiles: int) -> list[list[list[int | None]]]:
    result: list[list[list[int | None]]] = []
    for level in RESPONSIVE_MAP:
        out_level: list[list[int | None]] = []
        for row in level:
            out_row = [tile if (tile is not None and tile <= limit_tiles) else None for tile in row]
            out_level.append(out_row)
        result.append(out_level)
    return result
